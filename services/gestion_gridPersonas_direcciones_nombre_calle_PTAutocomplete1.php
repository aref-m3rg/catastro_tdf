//DataSourceClass_Initialize Event @2-5DE399D7
    function clscallesDataSource(& $Parent)
    {
        $this->Parent = & $Parent;
        $this->ErrorBlock = "Grid calles";
        $this->Initialize();
        $this->calle_nombre = new clsField("calle_nombre", ccsText, "");
        

    }
//End DataSourceClass_Initialize Event

//SetOrder Method @2-178022E4
    function SetOrder($SorterName, $SorterDirection)
    {
        $this->Order = "calle_nombre";
        $this->Order = CCGetOrder($this->Order, $SorterName, $SorterDirection, 
            "");
    }
//End SetOrder Method

//Prepare Method @2-671901B1
    function Prepare()
    {
        global $CCSLocales;
        global $DefaultDateFormat;
        $this->wp = new clsSQLParameters($this->ErrorBlock);
        $this->wp->AddParameter("1", "postnombre_calle", ccsText, "", "", $this->Parameters["postnombre_calle"], "", false);
        $this->wp->AddParameter("2", "expr605", ccsInteger, "", "", $this->Parameters["expr605"], "", false);
        $this->wp->Criterion[1] = $this->wp->Operation(opContains, "calle_nombre", $this->wp->GetDBValue("1"), $this->ToSQL($this->wp->GetDBValue("1"), ccsText),false);
        $this->wp->Criterion[2] = $this->wp->Operation(opEqual, "tipo_estado_id", $this->wp->GetDBValue("2"), $this->ToSQL($this->wp->GetDBValue("2"), ccsInteger),false);
        $this->Where = $this->wp->opAND(
             false, 
             $this->wp->Criterion[1], 
             $this->wp->Criterion[2]);
    }
//End Prepare Method

//Open Method @2-ECE7FD76
    function Open()
    {
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "BeforeBuildSelect", $this->Parent);
        $this->CountSQL = "SELECT COUNT(*)\n\n" .
        "FROM calles";
        $this->SQL = "SELECT calle_nombre \n\n" .
        "FROM calles {SQL_Where} {SQL_OrderBy}";
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "BeforeExecuteSelect", $this->Parent);
        if ($this->CountSQL) 
            $this->RecordsCount = CCGetDBValue(CCBuildSQL($this->CountSQL, $this->Where, ""), $this);
        else
            $this->RecordsCount = "CCS not counted";
        $this->query($this->OptimizeSQL(CCBuildSQL($this->SQL, $this->Where, $this->Order)));
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "AfterExecuteSelect", $this->Parent);
    }
//End Open Method

//SetValues Method @2-F0049829
    function SetValues()
    {
        $this->calle_nombre->SetDBValue($this->f("calle_nombre"));
    }
//End SetValues Method

} //End callesDataSource Class @2-FCB6E20C

