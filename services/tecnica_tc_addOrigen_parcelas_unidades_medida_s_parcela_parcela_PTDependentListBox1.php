//End DataSource Variables

//DataSourceClass_Initialize Event @2-A2687A4B
    function clsparcelasDataSource(& $Parent)
    {
        $this->Parent = & $Parent;
        $this->ErrorBlock = "Grid parcelas";
        $this->Initialize();
        $this->parcela_parcela = new clsField("parcela_parcela", ccsText, "");
        
        $this->parcela_parcela1 = new clsField("parcela_parcela1", ccsText, "");
        

    }
//End DataSourceClass_Initialize Event

//SetOrder Method @2-9783CF91
    function SetOrder($SorterName, $SorterDirection)
    {
        $this->Order = "parcela_parcela`* 1";
        $this->Order = CCGetOrder($this->Order, $SorterName, $SorterDirection, 
            "");
    }
//End SetOrder Method

//Prepare Method @2-406CDA03
    function Prepare()
    {
        global $CCSLocales;
        global $DefaultDateFormat;
        $this->wp = new clsSQLParameters($this->ErrorBlock);
        $this->wp->AddParameter("1", "urldpto_id", ccsInteger, "", "", $this->Parameters["urldpto_id"], "", false);
        $this->wp->AddParameter("2", "urlkeyword", ccsText, "", "", $this->Parameters["urlkeyword"], "", false);
        $this->wp->Criterion[1] = $this->wp->Operation(opEqual, "tipo_depto_parc_id", $this->wp->GetDBValue("1"), $this->ToSQL($this->wp->GetDBValue("1"), ccsInteger),false);
        $this->wp->Criterion[2] = $this->wp->Operation(opEqual, "parcela_macizo", $this->wp->GetDBValue("2"), $this->ToSQL($this->wp->GetDBValue("2"), ccsText),false);
        $this->Where = $this->wp->opAND(
             false, 
             $this->wp->Criterion[1], 
             $this->wp->Criterion[2]);
    }
//End Prepare Method

//Open Method @2-E1A38D76
    function Open()
    {
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "BeforeBuildSelect", $this->Parent);
        $this->SQL = "SELECT parcela_parcela, parcela_macizo \n\n" .
        "FROM parcelas {SQL_Where}\n\n" .
        "GROUP BY parcela_parcela {SQL_OrderBy}";
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "BeforeExecuteSelect", $this->Parent);
        if ($this->CountSQL) 
            $this->RecordsCount = CCGetDBValue(CCBuildSQL($this->CountSQL, $this->Where, ""), $this);
        else
            $this->RecordsCount = "CCS not counted";
        $this->query($this->OptimizeSQL(CCBuildSQL($this->SQL, $this->Where, $this->Order)));
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "AfterExecuteSelect", $this->Parent);
    }
//End Open Method

//SetValues Method @2-767BFCA9
    function SetValues()
    {
        $this->parcela_parcela->SetDBValue($this->f("parcela_parcela"));
        $this->parcela_parcela1->SetDBValue($this->f("parcela_parcela"));
    }
//End SetValues Method

} //End parcelasDataSource Class @2-FCB6E20C

