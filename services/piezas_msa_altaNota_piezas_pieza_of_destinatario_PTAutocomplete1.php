    function Prepare()
    {
        global $CCSLocales;
        global $DefaultDateFormat;
        $this->wp = new clsSQLParameters($this->ErrorBlock);
        $this->wp->AddParameter("1", "postpieza_of_destinatario", ccsText, "", "", $this->Parameters["postpieza_of_destinatario"], "", false);
